    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount() == 2){
            long cpf = Long.parseLong(contriTableModel.getContribuinte(jTable1.getSelectedRow()).getCpf());
          
            try {
                dados.Contribuinte c = controlador.select(cpf);
                new ContribuinteCadastro(null, true, c).setVisible(true);
             }  
             catch (Exception ex) {
                new Tratador().capturaExcecao(ex);
                new ErrorDialog(null, true).setVisible(true);
             } 
            finally{
                carregaTabela();
            }
        }

        
    }//GEN-LAST:event_jTable1MouseClicked

