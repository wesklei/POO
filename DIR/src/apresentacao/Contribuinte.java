    private void carregaTabela(){
        
        List<dados.Contribuinte> linhas;
      
        try {
            linhas = controlador.selectAll();                        
            
            contriTableModel = new ContribuinteTableModel(linhas);
            jTable1.setModel(contriTableModel);
            
        } catch (SQLException ex) {
            new Tratador().capturaExcecao(ex);
            
            new ErrorDialog(null, true).setVisible(true);
        }
        

        
    }
    
